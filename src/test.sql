CREATE TABLE Vehicles (
  Make VARCHAR(200),
  Model VARCHAR(200),
  Vehicle_Class VARCHAR(200),
  Engine_Size DECIMAL,
  Cylinders INTEGER,
  Transmission VARCHAR(10),
  Fuel_type VARCHAR(10),
  Fuel_consumption_city DECIMAL,
  Fuel_consumption_hwy DECIMAL,
  Combined_fuel_consumption DECIMAL,
  Fuel_consumption_mpg DECIMAL,
  Co2_emissions DECIMAL
); 